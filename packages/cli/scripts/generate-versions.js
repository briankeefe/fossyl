#!/usr/bin/env node
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const packagesDir = join(__dirname, '..', '..');

function getVersion(pkg) {
  const pkgJson = JSON.parse(readFileSync(join(packagesDir, pkg, 'package.json'), 'utf-8'));
  return pkgJson.version;
}

const versions = {
  core: getVersion('core'),
  express: getVersion('express'),
  zod: getVersion('zod'),
  kysely: getVersion('kysely'),
};

const content = `// Auto-generated by scripts/generate-versions.js - DO NOT EDIT
export const VERSIONS = {
  core: '${versions.core}',
  express: '${versions.express}',
  zod: '${versions.zod}',
  kysely: '${versions.kysely}',
} as const;
`;

writeFileSync(join(__dirname, '..', 'src', 'versions.ts'), content);
console.log('Generated src/versions.ts with versions:', versions);
